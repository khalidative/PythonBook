#====================================================

class SimpleClass:
    """An example of a simple class"""
    # Variable assignments
    a = 3.1415
    b = 10
    c = -12 * 3
    
    # Method definitions
    def __init__(self, x, y, z): # passing arguments to the init method
        self.a = x
        self.b = y
        self.c = z
    
    def sayHello(self):
        return "Hello user!"










































