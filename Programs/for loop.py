# Program to find the sum of all numbers in a list
#===============================================================

# List of integers
numlist = [10, 20, 30, 40, 50, 60, 70, 80, 90]

# variable to store the sum of elements
sum = 0

# iterate over the list
for num in numlist:
	sum = sum + num

# Output: The sum is 450
print("The sum of all elements is ", sum)































