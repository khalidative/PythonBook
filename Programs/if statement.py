# Demonstrating the if statement.
# If the number is positive, then print the appropriate message.
#===============================================================
num = 3
if num > 0:                             # if statements condtion
    print(num, "is a positive number.") # indentation marks the if block
    print("still in if block")          # indentation marks the if block
print("outside the if block.")          # outside the if block

num = -1

if num > 0:                             # if statements condtion
    print(num, "is a positive number.") # indentation marks the if block
print("outside the if block.")          # outside the if block

































